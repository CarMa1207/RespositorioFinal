package com.example.trabajofinal.Bucle;

public class Datos {

    int mutaciones;
    int longevo;
    int reproducciones;
    int MaxRep;
    int MaxMut;
    int MaxAgua;


    public int getMutaciones() {
        return mutaciones;
    }

    public void setMutaciones(int mutaciones) {
        this.mutaciones = mutaciones;
    }

    public int getLongevo() {
        return longevo;
    }

    public void setLongevo(int longevo) {
        this.longevo = longevo;
    }

    public int getReproducciones() {
        return reproducciones;
    }

    public void setReproducciones(int reproducciones) {
        this.reproducciones = reproducciones;
    }

    public int getMaxRep() {
        return MaxRep;
    }

    public void setMaxRep(int maxRep) {
        MaxRep = maxRep;
    }

    public int getMaxMut() {
        return MaxMut;
    }

    public void setMaxMut(int maxMut) {
        MaxMut = maxMut;
    }

    public int getMaxAgua() {
        return MaxAgua;
    }

    public void setMaxAgua(int maxAgua) {
        MaxAgua = maxAgua;
    }




}
